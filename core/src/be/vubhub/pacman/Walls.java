    private SpriteBatch batch;
    private Texture wall;
    private Rectangle pos = new Rectangle(0, 0, 50, 50);

    Walls() {
        batch = new SpriteBatch();
        wall = new Texture(Gdx.files.internal("wall.png"));
    }

    Walls(Rectangle pos) {
        this();
        this.pos = pos;
    }

    void render() {
        batch.begin();

        for (int y = 0; y < walls.length; y++) {
            for (int x = 0; x < walls[0].length; x++) {
                if (walls[y][x] == 1) {
                    batch.draw(wall, pos.x + pos.width*x, pos.y + pos.height*y);
                }
            }
        }

        batch.end();
    }

    void dispose() {
        wall.dispose();
        batch.dispose();
    }

    static int getOffset() {
        return 50;
    }
